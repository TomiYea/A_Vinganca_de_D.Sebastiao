def rsImage(image, angle, pivot, offset,scale):
    scaled = pygame.transform.scale(image,scale)
    rotated = pygame.transform.rotozoom(scaled, -angle, 1)
    rotated_offset = offset.rotate(angle)
    rect = rotated.get_rect(center=pivot+rotated_offset)
    return rotated, rect